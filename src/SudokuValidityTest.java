import static org.junit.jupiter.api.Assertions.assertTrue;

class SudokuValidityTest {
    // I wanted to *test* out how tests work, so I wrote this test...
    @org.junit.jupiter.api.Test
    void isValidSudoku() {
        char[][] board = new char[][]{
                {'5', '3', '.', '.', '7', '.', '.', '.', '.'},
                {'6', '.', '.', '1', '9', '5', '.', '.', '.'},
                {'.', '9', '8', '.', '.', '.', '.', '6', '.'},
                {'8', '.', '.', '.', '6', '.', '.', '.', '3'},
                {'4', '.', '.', '8', '.', '3', '.', '.', '1'},
                {'7', '.', '.', '.', '2', '.', '.', '.', '6'},
                {'.', '6', '.', '.', '.', '.', '2', '8', '.'},
                {'.', '.', '.', '4', '1', '9', '.', '.', '5'},
                {'.', '.', '.', '.', '8', '.', '.', '7', '9'}
        }; // This is a valid sudoku board
        assertTrue(SudokuValidity.isValidSudoku(board));
        char[][] board2 = new char[][]{{'8', '3', '.', '.', '7', '.', '.', '.', '.'},
                {'6', '.', '.', '1', '9', '5', '.', '.', '.'},
                {'.', '9', '8', '.', '.', '.', '.', '6', '.'},
                {'8', '.', '.', '.', '6', '.', '.', '.', '3'},
                {'4', '.', '.', '8', '.', '3', '.', '.', '1'},
                {'7', '.', '.', '.', '2', '.', '.', '.', '6'},
                {'.', '6', '.', '.', '.', '.', '2', '8', '.'},
                {'.', '.', '.', '4', '1', '9', '.', '.', '5'},
                {'.', '.', '.', '.', '8', '.', '.', '7', '9'}}; // This is not a valid sudoku board
        assertTrue(!SudokuValidity.isValidSudoku(board2));
        char[][] board3 = new char[][]{{'7', '9', '2', '1', '5', '4', '3', '8', '6'},
                {'6', '4', '3', '8', '2', '7', '1', '5', '9'},
                {'8', '5', '1', '3', '9', '6', '7', '2', '4'},
                {'2', '6', '5', '9', '7', '3', '8', '4', '1'},
                {'4', '8', '9', '5', '6', '1', '2', '7', '3'},
                {'3', '1', '7', '4', '8', '2', '9', '6', '5'},
                {'1', '3', '6', '7', '4', '8', '5', '9', '2'},
                {'9', '7', '4', '2', '1', '5', '6', '3', '8'},
                {'5', '2', '8', '6', '3', '9', '4', '1', '7'}}; // This is a valid board
        assertTrue(SudokuValidity.isValidSudoku(board3));
    }
}